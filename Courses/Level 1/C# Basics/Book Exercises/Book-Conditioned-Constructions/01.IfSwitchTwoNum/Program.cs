using System;

class Program
{
    static void Main(string[] args)
    {
        int firstNumber = 10;
        int secondNumber = 5;
        if (firstNumber > secondNumber)
        {
            Console.WriteLine(firstNumber / secondNumber);
        }
        else
        {
            Console.WriteLine("First number is bigger than the second");
        }
    }
}
//Да се напише if-конструкция, която проверява стойността на две целочислени променливи
//и разменя техните стойности, ако стойността на първата променлива е по-голяма от втората.